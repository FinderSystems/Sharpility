
namespace Sharpility.Net
{
    /// <summary>
    /// Consts of standard content types.
    /// </summary>
    public static class MediaTypes
    {
        /// <summary>
        /// ATOM XML
        /// </summary>
        public const string ApplicationAtomXml = "application/atom+xml";

        /// <summary>
        /// WWW Form.
        /// </summary>
        public const string ApplicationFormUrlencoded = "application/x-www-form-urlencoded";

        /// <summary>
        /// JSON.
        /// </summary>
        public const string ApplicationJson = "application/json";

        /// <summary>
        /// Stream.
        /// </summary>
        public const string ApplicationOctetStream = "application/octet-stream";

        /// <summary>
        /// XHTML.
        /// </summary>
        public const string ApplicationXhtmlXml = "application/xhtml+xml";

        /// <summary>
        /// XML.
        /// </summary>
        public const string ApplicationXml = "application/xml";

        /// <summary>
        /// GIF image.
        /// </summary>
        public const string ImageGif = "image/gif";

        /// <summary>
        /// JPEG image.
        /// </summary>
        public const string ImageJpeg = "image/jpeg";

        /// <summary>
        /// PNG image.
        /// </summary>
        public const string ImagePng = "image/png";

        /// <summary>
        /// Form.
        /// </summary>
        public const string MultipartFormData = "multipart/form-data";

        /// <summary>
        /// HTML.
        /// </summary>
        public const string TextHtml = "text/html";

        /// <summary>
        /// Plain text.
        /// </summary>
        public const string TextPlain = "text/plain";

        /// <summary>
        /// Text/Xml.
        /// </summary>
        public const string TextXml = "text/xml";
    }
}
